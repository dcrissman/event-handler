package org.esbtools.eventhandler.lightblue.testing;

import org.esbtools.eventhandler.lightblue.Identity;

import java.util.Objects;

/** Use when all events of a type can be merged with each other. */
public class TypeIdentity implements Identity {
    private final Class<?> type;

    public TypeIdentity(Class<?> type) {
        this.type = type;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        TypeIdentity that = (TypeIdentity) o;
        return Objects.equals(type, that.type);
    }

    @Override
    public int hashCode() {
        return Objects.hash(type);
    }

    @Override
    public String toString() {
        return "TypeIdentity{" +
                "type=" + type +
                '}';
    }
}
